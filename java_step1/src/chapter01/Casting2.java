package chapter01;

public class Casting2 {
	public static void main(String[] args) {
		byte num1 = 10;
		int num2 = num1;

		System.out.println("num1 : " + num1);
		System.out.println("num2 : " + num2);
		System.out.println("────────────────────────────────────────────────");
		int num3 = 20;
		float num4 = num3;
		System.out.println("num3 : " + num3);
		System.out.println("num4 : " + num4);
	}
}
