package chapter01;

public class CharacterEx3 {
	public static void main(String[] args) {
		int a = 65;
		System.out.println(a);
		System.out.println((char)a);
		System.out.println((char)a++);
		System.out.println((char)a);
		System.out.println((char)++a);
		System.out.println(a);
		System.out.println("─────────────────────────────────────────── ");
		int a2;
		a2 = 97;
		System.out.println(a2);
	}
}