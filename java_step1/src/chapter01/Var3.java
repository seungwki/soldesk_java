package chapter01;

public class Var3 {
	public static void main(String[] args) {
//		1. 변수 선언, 초기화는 따로 따로 가능하다.
		int a1;
		a1 = 10;
		System.out.println(a1);

//		2. 선언 초기화 한번에 해도 됨.
		int a2 = 14;
		System.out.println(a2);

//		3. 여러 변수 한 번에 초기화도 가능.
		int a3 = 3, a4 = 4;
		System.out.println(a3);
		System.out.println(a4);
	}
}
