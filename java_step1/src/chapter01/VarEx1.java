package chapter01;

public class VarEx1 {
	/*
	 * 과제 다음 코드에서 반복해서 나오는 숫자 5, 3을 다른 숫자로 한 번에 변경할 수 있도록 다음을 변수 num1, num2를 사용하도록
	 * 변경해보세요
		System.out.println(5+3);
		System.out.println(5-3);
		System.out.println(5*3);
	 */
	public static void main(String[] args) {
		int num1, num2;
		num1 = 5;num2 = 3;
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
	}
}
