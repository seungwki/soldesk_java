package chapter01;

public class VarEx2 {
	/*
	 * 변수 num1을 선언하고 10을 할당
	 * 변수 num2을 선언하고 20을 할당
	 * 두 변수의 합을 구해 그 결과를 새 변수 sum에 저장
	 * sum에 출력.
	 */
	public static void main(String[] args) {
		int num1, num2, sum;
		num1 = 10;
		num2 = 20;
		sum = num1+num2;
		System.out.println(sum);
	}
}