package chapter02;

public class RelationTest01 {
	public static void main(String[] args) {
		int num1, num2;
		num1 = 5;
		num2 = 3;
		System.out.println("num1>num2 : "+(num1>num2));
		System.out.println("num1<num2 : "+(num1<num2));
		System.out.println("num1>=num2 : "+(num1>=num2));
		System.out.println("num1<=num2 : "+(num1<=num2));
		System.out.println("num1==num2 : "+(num1==num2));
		System.out.println("num1!=num2 : "+(num1!=num2));
	}
}
