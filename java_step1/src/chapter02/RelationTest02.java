package chapter02;

public class RelationTest02 {
	public static void main(String[] args) {
		int num1, num2;
		num1 = 10;
		num2 = 10;
		System.out.println("num1>=num2 : "+(num1>=num2));
		System.out.println("num1<=num2 : "+(num1<=num2));
		System.out.println("num1==num2 : "+(num1==num2));
		System.out.println("num1!=num2 : "+(num1!=num2));
		System.out.println("───────────────────────────");
		char chr1, chr2;
		chr1 = 'A';chr2 = 'B';
		System.out.println("chr1>=chr2 : "+(chr1>=chr2));
		System.out.println("chr1<=chr2 : "+(chr1<=chr2));
		System.out.println("chr1==chr2 : "+(chr1==chr2));
		System.out.println("chr1!=chr2 : "+(chr1!=chr2));
		
	}
}
